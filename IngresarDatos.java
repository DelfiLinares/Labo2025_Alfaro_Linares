public class IngresarDatos {
    public static void main(String[] args) {
        int n=4;
        double a=-1.56;
        char c='M';
        System.out.println(n);
        System.out.println(a);
        System.out.println(c);
        double suma=0;
        suma=n+a;
        System.out.println("Suma n+a: "+ suma);
        double dif=0;
        dif=a-n;
        System.out.println("Diferencia a-n: "+dif);
        int ascii=c;
        System.out.println("ASCII de "+c+": "+ascii);

    }
}
